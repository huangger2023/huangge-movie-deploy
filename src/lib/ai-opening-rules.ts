export function buildOpeningRetentionRules(movieTitle: string, targetChars = "300-400") {
  return [
    "【短视频留存开头硬规则】",
    `目标字数：每个开头约 ${targetChars} 字。`,
    "",
    "1. 【最高铁律】前3秒禁止直接报片名、角色名、演员名、年份、国家、奖项、评分或名场面。",
    "2. 开头前 80-120 个汉字必须先抓住陌生观众，而不是服务已经看过电影的人。",
    "   先写人人都能代入的情绪痛点、日常生活场景、反常识设定或人性困境，让观众先产生「这说的不就是我吗」或「这事真怪」的心理反应。",
    "3. 电影名和具体人物只能在观众已经被钩住之后自然浮出水面（在第4句或之后）。",
    "   推荐顺序：普适痛点/反常设定 -> 具体画面 -> 人性困境 -> 悬念问题 -> 自然引出电影和剧情。",
    "4. 不要像新闻播报、百科介绍、剧情梗概或影评开场。",
    "   禁止一上来解释背景、罗列身份、介绍主角、介绍电影地位、直接复述案件/名场面。",
    "5. 每个开头的第一句话必须脱离电影名也成立，像一句能单独刷到就停住的短视频钩子。",
    "6. 可以使用第二人称、生活化场景和人性判断，但必须忠于真实剧情依据，不得编造事实。",
  ].join("\n");
}

export function buildOpeningRetentionChecklist(movieTitle: string) {
  return [
    "【生成前自检】",
    "- 第一段前 80-120 个汉字没有出现任何片名或陌生人名。",
    "- 第一句话不是片名播报、人物履历、年份背景、奖项评分或名场面复述。",
    "- 观众即使没看过这部电影，也能先被痛点、困境、反常设定或画面感吸住。",
    "- 电影名和人物是在铺垫之后自然引出，而不是开场硬丢。",
    "- 如果违反任意一条，必须重写，不要输出违规版本。",
  ].join("\n");
}
